let usedShares: Set<string> = new Set();
const usedSharesByOptions = new WeakMap<NormalizedModuleFederationOptions, Set<string>>();

function getScopedUsedShares(options: NormalizedModuleFederationOptions) {
  let scoped = usedSharesByOptions.get(options);
  if (!scoped) {
    scoped = new Set();
    usedSharesByOptions.set(options, scoped);
  }
  return scoped;
}

export function getUsedShares(options?: NormalizedModuleFederationOptions) {
  if (options) return getScopedUsedShares(options);
  return usedShares;
}
export function addUsedShares(pkg: string, options?: NormalizedModuleFederationOptions) {
  usedShares.add(pkg);
  if (options) getScopedUsedShares(options).add(pkg);
}
const LOCAL_SHARED_IMPORT_MAP_ID = 'virtual:mf-localSharedImportMap';

const localOwnerIds = new WeakMap<NormalizedModuleFederationOptions, number>();
let nextLocalOwnerId = 1;

function getLocalOwnerKey(options: NormalizedModuleFederationOptions) {
  let ownerId = localOwnerIds.get(options);
  if (!ownerId) {
    ownerId = nextLocalOwnerId++;
    localOwnerIds.set(options, ownerId);
  }
  return `${options.internalName}__mf_owner__${ownerId}`;
}

export function getLocalSharedImportMapPath(options?: NormalizedModuleFederationOptions) {
  const resolvedOptions = options ?? getNormalizeModuleFederationOptions();
  const ownerName = options
    ? getLocalOwnerKey(resolvedOptions)
    : resolvedOptions.internalName || resolvedOptions.name;
  return `${LOCAL_SHARED_IMPORT_MAP_ID}:${packageNameEncode(ownerName)}`;
}

export function getResolvedLocalSharedImportMapId(options?: NormalizedModuleFederationOptions) {
  return `\0${getLocalSharedImportMapPath(options)}`;
}

let invalidateLocalSharedImportMap: (() => void) | undefined;
const localSharedImportMapInvalidators = new WeakMap<
  NormalizedModuleFederationOptions,
  () => void
>();
export function setLocalSharedImportMapInvalidator(
  invalidator: (() => void) | undefined,
  options?: NormalizedModuleFederationOptions
) {
  if (!options) {
    invalidateLocalSharedImportMap = invalidator;
  } else if (invalidator) {
    localSharedImportMapInvalidators.set(options, invalidator);
  } else {
    localSharedImportMapInvalidators.delete(options);
  }
}

export function writeLocalSharedImportMap(options?: NormalizedModuleFederationOptions) {
  // The map is generated by the loader. Avoid rebuilding a large string every
  // time another shared module is discovered during resolution.
  (options ? localSharedImportMapInvalidators.get(options) : invalidateLocalSharedImportMap)?.();
}

function shouldUseDirectReactImport() {
  const isVinext = hasPackageDependency('vinext');
  const isAstro = hasPackageDependency('astro');
  return isVinext || isAstro;
}

