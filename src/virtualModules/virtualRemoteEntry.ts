export function generateLocalSharedImportMap(options?: NormalizedModuleFederationOptions) {
  const resolvedOptions = options ?? getNormalizeModuleFederationOptions();
  const useDirectReactImport = shouldUseDirectReactImport();
  const orderedShares = getOrderedUsedShares(options);
  const eagerImports = orderedShares
    .map((pkg, index) => {
      const shareItem = getNormalizeShareItem(pkg, resolvedOptions);
      if (!shareItem?.shareConfig.eager || shareItem.shareConfig.import === false) return '';
      return `import * as __mfEagerShare_${index} from ${JSON.stringify(
        getLocalSharedPackagePath(pkg, shareItem, options)
      )};`;
    })
    .filter(Boolean)
    .join('\n');

  return `
    import {loadShare} from "@module-federation/runtime";
    ${eagerImports}
    const importMap = {
      ${orderedShares
        .map((pkg, index) => {
          const shareItem = getNormalizeShareItem(pkg, resolvedOptions);
          return `
        ${JSON.stringify(pkg)}: async () => {
          ${
            shareItem?.shareConfig.import === false
              ? `throw new Error(\`[Module Federation] Shared module '\${${JSON.stringify(pkg)}}' must be provided by host\`);`
              : shareItem?.shareConfig.eager
                ? `let pkg = __mfEagerShare_${index};
            return pkg;`
                : `let pkg = await import(${JSON.stringify(getLocalSharedPackagePath(pkg, shareItem, options))});
            return pkg;`
          }
        }
      `;
        })
        .join(',')}
    }
      const usedShared = {
      ${getOrderedUsedShares(options)
        .map((key) => {
          const shareItem = getNormalizeShareItem(key, resolvedOptions);
          if (!shareItem) return null;
          const detectedNamedExports = getSharedNamedExports(key, shareItem);
          const canLiveRebind =
            shareItem.shareConfig.import === false || detectedNamedExports !== undefined;
          // A partial runtime provider cannot back an ESM proxy whose complete
          // export surface is unknown. Keep that share on its coherent local
          // namespace instead of selecting a different tree provider.
          const treeShakingConfig = canLiveRebind ? shareItem.shareConfig.treeShaking : undefined;
          const treeShakingUsage = treeShakingConfig
            ? getTreeShakingExportUsage(key, shareItem, shareItem.name, options)
            : undefined;
          const treeShakingProviderExports =
            treeShakingUsage?.kind === 'exports' ? treeShakingUsage.usedExports : [];
          const treeShakingUsedExports =
            resolvedOptions.injectTreeShakingUsedExports === false
              ? treeShakingConfig?.usedExports || []
              : treeShakingProviderExports;
          // Runtime inference is only sound when the consumer publishes the
          // exports discovered by this build. If injection is disabled, keep
          // the complete provider as the safe fallback instead of advertising
          // a partial provider with incomplete coverage metadata.
          const disableRuntimeInference =
            treeShakingConfig?.mode === 'runtime-infer' &&
            resolvedOptions.injectTreeShakingUsedExports === false;
          const treeShakingProviderImportId =
            treeShakingConfig &&
            !disableRuntimeInference &&
            hasTreeShakingSharedProvider(key, shareItem, options)
              ? getTreeShakingSharedProviderImportId(key, options)
              : undefined;
          const treeShakingStatus =
            treeShakingUsage?.kind === 'full' ||
            disableRuntimeInference ||
            (treeShakingConfig?.mode === 'runtime-infer' &&
              !treeShakingProviderImportId &&
              shareItem.shareConfig.import !== false)
              ? 0
              : 1;
          return `
          ${JSON.stringify(key)}: {
            name: ${JSON.stringify(key)},
            version: ${JSON.stringify(shareItem.version)},
            scope: [${JSON.stringify(shareItem.scope)}],
            loaded: false,
            eager: ${Boolean(shareItem.shareConfig.eager)},
            from: ${JSON.stringify(resolvedOptions.name)},
            canLiveRebind: ${canLiveRebind},
            async get () {
              if (${shareItem.shareConfig.import === false}) {
                throw new Error(\`[Module Federation] Shared module '\${${JSON.stringify(key)}}' must be provided by host\`);
              }
              usedShared[${JSON.stringify(key)}].loaded = true
              const {${JSON.stringify(key)}: pkgDynamicImport} = importMap
              const res = await pkgDynamicImport()
              const exportModule = ${JSON.stringify(useDirectReactImport)} && ${JSON.stringify(key)} === "react"
                ? (res?.default ?? res)
                : {...res}
              // All npm packages pre-built by vite will be converted to esm
              if (exportModule.__esModule !== true) {
                Object.defineProperty(exportModule, "__esModule", {
                  value: true,
                  enumerable: false
                })
              }
              return function () {
                return exportModule
              }
            },
            shareConfig: {
              singleton: ${shareItem.shareConfig.singleton},
              requiredVersion: ${JSON.stringify(shareItem.shareConfig.requiredVersion)},
              strictVersion: ${shareItem.shareConfig.strictVersion},
              eager: ${Boolean(shareItem.shareConfig.eager)},
              ${shareItem.shareConfig.import === false ? 'import: false,' : ''}
            },
            ${
              treeShakingConfig
                ? `treeShaking: {
              mode: ${JSON.stringify(treeShakingConfig.mode)},
              usedExports: ${JSON.stringify(treeShakingUsedExports)},
              providedExports: ${JSON.stringify(treeShakingProviderExports)},
              status: ${treeShakingStatus},
              ${
                treeShakingProviderImportId
                  ? `async get() {
                const container = await import(${JSON.stringify(treeShakingProviderImportId)});
                if (typeof container.init === "function") await container.init();
                return container.get();
              },`
                  : ''
              }
            }`
                : ''
            }
          }
        `;
        })
        .filter((x) => x !== null)
        .join(',')}
    }
      const usedRemotes = [${Object.keys(getUsedRemotesMap(options))
        .map((key) => {
          const remote = resolvedOptions.remotes[key];
          if (!remote) return null;
          return `
                {
                  alias: ${JSON.stringify(key)},
                  entryGlobalName: ${JSON.stringify(remote.entryGlobalName)},
                  name: ${JSON.stringify(remote.name)},
                  type: ${JSON.stringify(remote.type)},
                  entry: ${JSON.stringify(remote.entry)},
                  shareScope: ${JSON.stringify(remote.shareScope ?? 'default')},
                }
          `;
        })
        .filter((x) => x !== null)
        .join(',')}
      ]
      export {
        usedShared,
        usedRemotes
      }
      `;
}

function getOrderedUsedShares(options?: NormalizedModuleFederationOptions) {
  const resolvedOptions = options ?? getNormalizeModuleFederationOptions();
  const shares = new Set(getUsedShares(options));
  Object.keys(resolvedOptions.shared).forEach((pkg) => {
    if (!pkg.endsWith('/')) shares.add(pkg);
  });
  const sorted = Array.from(shares).sort((a, b) => {
    const priority = (pkg: string) =>
      pkg === 'react' ? 0 : pkg === 'react-dom' ? 1 : pkg.startsWith('react/') ? 2 : 3;
    return priority(a) - priority(b) || a.localeCompare(b);
  });
  return orderSharedDependenciesFirst(sorted);
}

function orderSharedDependenciesFirst(sharedPackages: string[]) {
  const sharedKeyByPackageName = new Map<string, string>();
  const subpathKeysByPackageName = new Map<string, string[]>();
  sharedPackages.forEach((pkg) => {
    const packageName = getPackageName(pkg);
    const existing = sharedKeyByPackageName.get(packageName);
    if (!existing || pkg === packageName) {
      sharedKeyByPackageName.set(packageName, pkg);
    }
    if (pkg !== packageName) {
      const subpaths = subpathKeysByPackageName.get(packageName);
      if (subpaths) subpaths.push(pkg);
      else subpathKeysByPackageName.set(packageName, [pkg]);
    }
  });
  const visiting = new Set<string>();
  const visited = new Set<string>();
  const ordered: string[] = [];
  const visit = (pkg: string) => {
    if (visited.has(pkg)) return;
    if (visiting.has(pkg)) return;
    visiting.add(pkg);
    const packageName = getPackageName(pkg);
    const packageJson =
      getInstalledPackageJson(pkg)?.packageJson ??
      (pkg !== packageName ? getInstalledPackageJson(packageName)?.packageJson : undefined);
    const dependencies = {
      ...((packageJson?.dependencies as Record<string, string> | undefined) || {}),
      ...((packageJson?.peerDependencies as Record<string, string> | undefined) || {}),
      ...((packageJson?.optionalDependencies as Record<string, string> | undefined) || {}),
    };
    Object.keys(dependencies).forEach((dependency) => {
      const sharedDependency = sharedKeyByPackageName.get(dependency);
      if (sharedDependency) visit(sharedDependency);
    });
    // Most packages can consume their own shared subpath exports through
    // self-referencing bare specifiers at module-evaluation time, so subpath
    // share keys must come before their package root (see #767, #805).
    // React's subpath implementations have the opposite dependency: React 18
    // evaluates react/jsx-runtime against ReactSharedInternals from the root
    // package. Initializing the subpath first crashes in dev (see #924).
    if (pkg === packageName) {
      const subpaths = subpathKeysByPackageName.get(packageName) || [];
      const rootMustBeInitializedFirst = packageName === 'react' || packageName === 'react-dom';
      if (rootMustBeInitializedFirst) {
        visiting.delete(pkg);
        visited.add(pkg);
        ordered.push(pkg);
        subpaths.forEach(visit);
        return;
      }
      subpaths.forEach(visit);
    }
    visiting.delete(pkg);
    visited.add(pkg);
    ordered.push(pkg);
  };
  sharedPackages.forEach(visit);
  return ordered;
}

function getShareItemForPreload(
  pkg: string,
  options: NormalizedModuleFederationOptions = getNormalizeModuleFederationOptions()
) {
  const shared = options.shared;
  const packageName = pkg.startsWith('@')
    ? pkg.split('/').slice(0, 2).join('/')
    : pkg.split('/')[0];
  const wildcardKey = `${packageName}/`;

  if (isExplicitSharedKey(pkg, options)) return shared[pkg];
  if (isExplicitSharedKey(wildcardKey, options)) return shared[wildcardKey];
  return undefined;
}

